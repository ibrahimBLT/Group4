public class GoldFish extends Fish{

    public GoldFish(){};
    public GoldFish(String name, String bodyCovering, String color, char gender, int age,
                    int numberOfLegs, String size, double weight, String speed, String habitatArea,
                    boolean canFly, boolean canSwim, String fat, int fin) {
        super(name, "Fish", bodyCovering, color, gender, age, numberOfLegs, size,
                weight, speed, habitatArea, canFly, canSwim, fat, fin);
    }
}
